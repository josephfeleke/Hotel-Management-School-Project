    private void populate7DChart(){
           try {
            
            
            ResultSet maxRevenueResult = SqlConnection.statement.executeQuery("select max(amount) as mexRev from revenue");
            int maxRevenue = 0;
            while(maxRevenueResult.next())
                 maxRevenue = maxRevenueResult.getInt(1);
            
            System.out.println(maxRevenue);
            
            panelChartLine.removeAllData();
            
            ModelChartLine m[] = new ModelChartLine[7];
            
            ResultSet result;
            
            
            
            result = SqlConnection.statement.executeQuery("select amount,day from revenue order by revid desc limit 7");
            Revenue revenue[] = new Revenue[7];
            
            
        int k =0;
            while(result.next()){
           
              
                    
                  revenue[k] = new Revenue();                  
                  revenue[k].setamount(result.getString(1));
                  System.out.println(result.getString(1));
                  revenue[k].setDate(result.getString(2));
                   k++;
       
                }
            
            ArrayList<Integer> amount = new ArrayList<>();
            for(int i = 0;i<7;i++)
                amount.add(Integer.parseInt(revenue[i].getamount()));
            
            
            
            
            int maxPrice = Collections.max(amount);
            int price6 = (int) ((maxPrice/7)*(6));
            int price5 = (int) ((maxPrice/7)*(5));
            int price4 = (int) ((maxPrice/7)*(4));
            int price3 = (int) ((maxPrice/7)*(3));
            int price2 = (int) ((maxPrice/7)*(2));
            int price1 = (int) ((maxPrice/7)*(1));
            
            graphPriceMax.setText(String.valueOf(maxPrice));
            graphPrice6.setText(String.valueOf(price6));
            graphPrice5.setText(String.valueOf(price5));
            graphPrice4.setText(String.valueOf(price4));
            graphPrice3.setText(String.valueOf(price3));
            graphPrice2.setText(String.valueOf(price2));
            graphPrice1.setText(String.valueOf(price1));
                
            
         
            
            
            
            for(int i =0; i<7; i++){
                m[i]= new ModelChartLine();
                m[i].setName("monday");
                if(i==0){
                    m[i].setValue(Integer.parseInt(revenue[6].getamount()));
                    graphDay1.setText(revenue[6].getDate());
                }
                if(i==1){
                    m[i].setValue(Integer.parseInt(revenue[5].getamount()));
                    graphDay2.setText(revenue[5].getDate());
                }
                if(i==2){
                    m[i].setValue(Integer.parseInt(revenue[4].getamount()));
                    graphDay3.setText(revenue[4].getDate());
                }
                if(i==3){
                    m[i].setValue(Integer.parseInt(revenue[3].getamount()));
                    graphDay4.setText(revenue[3].getDate());
                }
                if(i==4){
                    m[i].setValue(Integer.parseInt(revenue[2].getamount()));
                    graphDay5.setText(revenue[2].getDate());
                }
                if(i==5){
                    m[i].setValue(Integer.parseInt(revenue[1].getamount()));
                    graphDay6.setText(revenue[1].getDate());
                }
                if(i==6){
                    m[i].setValue(Integer.parseInt(revenue[0].getamount()));
                    graphDay7.setText(revenue[0].getDate());
                }
             
                panelChartLine.addItem(m[i]);
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
