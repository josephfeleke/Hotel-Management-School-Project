    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked
        String username = (String)usernameTextField.getText();
        String password = (String)passwordTextField.getText();

        ResultSet result;
        try {
            result = SqlConnection.statement.executeQuery("SELECT userid FROM appUser WHERE userNAME ="+"'"+username+"'"+"AND PASSword="+"'"+password+"'");
            if(result.next()){
                mainTabbedPanel.setSelectedIndex(1);
                loginStatus.setText("");
                System.out.println("Login successful!");
            }else
            loginStatus.setText("Wrong Password or Username, Try Again.");
            System.out.println("Wrong Name or Password!");

        } catch (SQLException ex) {
            Logger.getLogger(LoginPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loginButtonMouseClicked

